package lottery.third3d;

public class Omission3D {
    private String openNums;
    private String openCount;

    public Omission3D(String openNums, String openCount) {
        this.openNums = openNums;
        this.openCount = openCount;
    }

    public String getOpenNums() {
        return openNums;
    }

    public void setOpenNums(String openNums) {
        this.openNums = openNums;
    }

    public String getOpenCount() {
        return openCount;
    }

    public void setOpenCount(String openCount) {
        this.openCount = openCount;
    }
}
